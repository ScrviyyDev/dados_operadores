// Operadores Aritméticos, atribuição e incremento.

/**
 * Operadores Aritméticos
 * + Adição / Concatenação
 * - Subtração
 * / Divisão 
 * % Resto da divisão
 * * Multiplicação
 * ** Potenciação
 */

const num1 = 10;
const num2 = 3;
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);